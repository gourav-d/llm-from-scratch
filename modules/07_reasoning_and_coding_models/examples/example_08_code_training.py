class CodeDataPreprocessor:
    """
    Preprocesses code for training.

    In C#:
    class CodeDataPreprocessor {
        List<string> CleanCode(List<string> rawCode);
    }
    """

    def __init__(self):
        self.stats = {
            'total': 0,
            'filtered': 0,
            'cleaned': 0
        }

    def is_good_quality(self, code: str) -> bool:
        """
        Check if code is suitable for training.

        Filters out:
        - Too short/long code
        - Auto-generated code
        - Minified code
        - Code with too many blank lines

        In C#:
        public bool IsGoodQuality(string code) {
            return CheckLength(code) &&
                   !IsAutoGenerated(code) &&
                   !IsMinified(code);
        }
        """
        # Too short (not useful)
        if len(code.strip()) < 50:
            return False

        # Too long (probably generated or concatenated files)
        if len(code) > 10000:
            return False

        lines = code.split('\n')

        # Check blank line ratio
        non_blank_lines = [l for l in lines if l.strip()]
        if len(lines) == 0:
            return False

        blank_ratio = 1 - (len(non_blank_lines) / len(lines))
        if blank_ratio > 0.6:  # More than 60% blank
            return False

        # Check for auto-generated markers
        code_lower = code.lower()
        auto_markers = [
            'auto-generated',
            'do not edit',
            'generated by',
            'autogenerated',
            '// <auto-generated>',
        ]

        if any(marker in code_lower for marker in auto_markers):
            return False

        # Check for minified code (very long lines)
        max_line_len = max((len(l) for l in lines), default=0)
        if max_line_len > 500:
            return False

        # Must have some code structure (functions/classes)
        has_structure = any(
            keyword in code for keyword in
            ['def ', 'class ', 'function ', 'public ', 'private ']
        )

        if not has_structure:
            return False

        return True

    def clean_code(self, code: str) -> str:
        """
        Clean code for training.

        - Remove excessive blank lines
        - Normalize indentation
        - Remove trailing whitespace

        In C#:
        public string CleanCode(string code) {
            return code
                .RemoveExcessiveBlankLines()
                .NormalizeIndentation()
                .TrimLines();
        }
        """
        lines = code.split('\n')

        # Remove trailing whitespace from each line
        # In C#: lines.Select(l => l.TrimEnd())
        lines = [l.rstrip() for l in lines]

        # Remove excessive consecutive blank lines (max 2)
        cleaned_lines = []
        blank_count = 0

        for line in lines:
            if not line.strip():
                blank_count += 1
                if blank_count <= 2:  # Keep max 2 blank lines
                    cleaned_lines.append(line)
            else:
                blank_count = 0
                cleaned_lines.append(line)

        # Join back
        cleaned = '\n'.join(cleaned_lines)

        return cleaned

    def process_dataset(self, code_samples: List[str]) -> List[str]:
        """
        Process entire dataset.

        Args:
            code_samples: List of raw code snippets

        Returns:
            List of cleaned, filtered code snippets

        In C#:
        public List<string> ProcessDataset(List<string> samples) {
            return samples
                .Where(IsGoodQuality)
                .Select(CleanCode)
                .ToList();
        }
        """
        self.stats['total'] = len(code_samples)

        # Filter and clean
        processed = []

        for code in code_samples:
            if self.is_good_quality(code):
                cleaned = self.clean_code(code)
                processed.append(cleaned)
            else:
                self.stats['filtered'] += 1

        self.stats['cleaned'] = len(processed)

        return processed

    def get_stats(self) -> Dict:
        """Get preprocessing statistics."""
        return self.stats.copy()


