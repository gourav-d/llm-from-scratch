class FIMTransformer:
    """
    Transforms code into Fill-in-the-Middle format.

    FIM format: <fim_prefix>PREFIX<fim_suffix>SUFFIX<fim_middle>MIDDLE

    In C#:
    class FIMTransformer {
        (string prefix, string middle, string suffix) Transform(string code);
    }
    """

    def __init__(self, fim_ratio: float = 0.3):
        """
        Initialize FIM transformer.

        Args:
            fim_ratio: Percentage of samples to apply FIM (default 30%)
        """
        self.fim_ratio = fim_ratio
        self.prefix_token = "<fim_prefix>"
        self.suffix_token = "<fim_suffix>"
        self.middle_token = "<fim_middle>"

    def create_fim_sample(self, code: str) -> Optional[Tuple[str, str, str]]:
        """
        Split code into (prefix, middle, suffix) for FIM training.

        Example:
            Input: "def add(x, y):\\n    return x + y"

            Output:
            - prefix: "def add(x, y):"
            - middle: "\\n    return "
            - suffix: "x + y"

        In C#:
        public (string, string, string)? CreateFIMSample(string code) {
            var splits = FindSplitPoints(code);
            if (splits == null) return null;
            return (prefix, middle, suffix);
        }
        """
        lines = code.split('\n')

        # Need at least 3 lines for meaningful FIM
        if len(lines) < 3:
            return None

        # Choose two split points
        # split1: where prefix ends
        # split2: where middle ends (suffix begins)

        split1 = random.randint(1, len(lines) - 2)
        split2 = random.randint(split1 + 1, len(lines))

        # Create prefix, middle, suffix
        prefix = '\n'.join(lines[:split1])
        middle = '\n'.join(lines[split1:split2])
        suffix = '\n'.join(lines[split2:])

        return prefix, middle, suffix

    def format_fim_training_sample(self, prefix: str, middle: str, suffix: str) -> str:
        """
        Format FIM sample for training.

        Format: <fim_prefix>PREFIX<fim_suffix>SUFFIX<fim_middle>MIDDLE

        The model will learn to predict MIDDLE given PREFIX and SUFFIX.

        In C#:
        public string FormatFIMTrainingSample(
            string prefix, string middle, string suffix) {
            return $"{PrefixToken}{prefix}{SuffixToken}{suffix}{MiddleToken}{middle}";
        }
        """
        formatted = (
            f"{self.prefix_token}{prefix}"
            f"{self.suffix_token}{suffix}"
            f"{self.middle_token}{middle}"
        )

        return formatted

    def transform_batch(self, code_samples: List[str]) -> List[Dict]:
        """
        Transform batch of code into training samples.

        Args:
            code_samples: List of code snippets

        Returns:
            List of training samples (mix of CLM and FIM)

        In C#:
        public List<TrainingSample> TransformBatch(List<string> codeSamples) {
            return codeSamples.Select(code =>
                ShouldApplyFIM(code) ? CreateFIMSample(code) : CreateCLMSample(code)
            ).ToList();
        }
        """
        training_samples = []

        for code in code_samples:
            # Randomly decide if this sample gets FIM treatment
            use_fim = random.random() < self.fim_ratio

            if use_fim:
                # Create FIM sample
                fim_parts = self.create_fim_sample(code)

                if fim_parts is not None:
                    prefix, middle, suffix = fim_parts
                    formatted = self.format_fim_training_sample(
                        prefix, middle, suffix
                    )

                    training_samples.append({
                        'type': 'FIM',
                        'input': formatted,
                        'target': middle,
                        'original': code
                    })
            else:
                # Standard causal language modeling (CLM)
                training_samples.append({
                    'type': 'CLM',
                    'input': code,
                    'target': code,  # Predict next token from previous
                    'original': code
                })

        return training_samples


