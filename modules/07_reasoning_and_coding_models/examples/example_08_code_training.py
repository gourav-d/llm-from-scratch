class DatasetStatistics:
    """
    Calculate statistics about code dataset.

    In C#:
    class DatasetStatistics {
        Dictionary<string, int> CalculateStats(List<string> dataset);
    }
    """

    @staticmethod
    def calculate_stats(dataset: List[str]) -> Dict:
        """
        Calculate dataset statistics.

        Returns:
            Dictionary with statistics

        In C#:
        public static Dictionary<string, object> CalculateStats(
            List<string> dataset) {
            return new Dictionary<string, object> {
                {"total_samples", dataset.Count},
                {"avg_length", dataset.Average(s => s.Length)},
                {"token_count", dataset.Sum(s => s.Split().Length)}
            };
        }
        """
        if not dataset:
            return {}

        # Basic stats
        total = len(dataset)
        lengths = [len(code) for code in dataset]
        line_counts = [len(code.split('\n')) for code in dataset]

        # Token count (rough estimate)
        token_counts = [
            len(code.replace('\n', ' ').split()) for code in dataset
        ]

        # Keyword frequency
        all_keywords = []
        for code in dataset:
            keywords = re.findall(r'\b(def|class|return|if|for|while|import)\b', code)
            all_keywords.extend(keywords)

        keyword_freq = Counter(all_keywords)

        stats = {
            'total_samples': total,
            'total_characters': sum(lengths),
            'avg_length': np.mean(lengths),
            'min_length': min(lengths),
            'max_length': max(lengths),
            'avg_lines': np.mean(line_counts),
            'total_tokens': sum(token_counts),
            'avg_tokens': np.mean(token_counts),
            'keyword_frequency': dict(keyword_freq.most_common(10))
        }

        return stats


