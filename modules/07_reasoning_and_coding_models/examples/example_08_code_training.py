    print(f"  Target: {fim_example['target'][:50]}...")
    print()

    # STEP 4: Dataset Statistics
    print("-" * 70)
    print("STEP 4: Dataset Statistics")
    print("-" * 70)
    print()

    # Calculate stats on original code (for readability)
    original_codes = [s['original'] for s in training_samples]
    dataset_stats = DatasetStatistics.calculate_stats(original_codes)

