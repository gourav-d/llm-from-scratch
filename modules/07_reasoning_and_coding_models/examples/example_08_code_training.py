class SimpleCodeGenerator:
    """
    Simple n-gram based code generator for demonstration.

    In real training, you'd use transformers, but this shows the concept.

    In C#:
    class SimpleCodeGenerator {
        Dictionary<string, List<string>> ngramModel;
        string Generate(string prompt);
    }
    """

    def __init__(self, n: int = 3):
        """
        Initialize generator with n-gram size.

        Args:
            n: Size of n-grams (default 3 = trigrams)
        """
        self.n = n
        self.ngram_model = {}  # (token1, token2, ...) -> [next_token1, next_token2, ...]

    def tokenize(self, code: str) -> List[str]:
        """
        Simple tokenization.

        In C#:
        public List<string> Tokenize(string code) {
            return code.Split(new[] {' ', '\n', '\t'});
        }
        """
        # Split on whitespace and newlines
        tokens = code.replace('\n', ' \n ').split()
        return [t for t in tokens if t]

    def train(self, code_samples: List[str]):
        """
        Train n-gram model on code samples.

        In C#:
        public void Train(List<string> codeSamples) {
            foreach (var code in codeSamples) {
                var tokens = Tokenize(code);
                UpdateNGramModel(tokens);
            }
        }
        """
        print(f"Training {self.n}-gram model on {len(code_samples)} samples...")

        for code in code_samples:
            tokens = self.tokenize(code)

            # Build n-grams
            for i in range(len(tokens) - self.n):
                # Context: n-1 tokens
                context = tuple(tokens[i:i + self.n - 1])
                # Next token
                next_token = tokens[i + self.n - 1]

                # Add to model
                if context not in self.ngram_model:
                    self.ngram_model[context] = []

                self.ngram_model[context].append(next_token)

        print(f"Learned {len(self.ngram_model)} n-gram patterns")

    def generate(self, prompt: str, max_tokens: int = 50) -> str:
        """
        Generate code from prompt.

        Args:
            prompt: Starting code
            max_tokens: Maximum tokens to generate

        Returns:
            Generated code

        In C#:
        public string Generate(string prompt, int maxTokens = 50) {
            var tokens = Tokenize(prompt).ToList();
            for (int i = 0; i < maxTokens; i++) {
                var nextToken = PredictNextToken(tokens);
                if (nextToken == null) break;
                tokens.Add(nextToken);
            }
            return string.Join(" ", tokens);
        }
        """
        tokens = self.tokenize(prompt)

        generated_tokens = 0

        while generated_tokens < max_tokens:
            # Get context (last n-1 tokens)
            if len(tokens) < self.n - 1:
                break

            context = tuple(tokens[-(self.n - 1):])

            # Predict next token
            if context in self.ngram_model:
                # Choose randomly from possible next tokens
                next_token = random.choice(self.ngram_model[context])
                tokens.append(next_token)
                generated_tokens += 1

                # Stop at function end (simple heuristic)
                if next_token == '\n' and len(tokens) > 10:
                    # Check if we're done (rough heuristic)
                    recent = ' '.join(tokens[-5:])
                    if 'return' in recent:
                        break
            else:
                # No prediction available
                break

        # Join tokens back
        code = ' '.join(tokens)

        # Clean up spacing around newlines
        code = code.replace(' \n ', '\n')

        return code


