    print(f"  Total samples: {dataset_stats['total_samples']}")
    print(f"  Average length: {dataset_stats['avg_length']:.0f} chars")
    print(f"  Average lines: {dataset_stats['avg_lines']:.1f}")
    print(f"  Total tokens: {dataset_stats['total_tokens']}")
    print(f"  Average tokens: {dataset_stats['avg_tokens']:.0f}")
    print()
    print("Top keywords:")
    for keyword, count in dataset_stats['keyword_frequency'].items():
        print(f"  {keyword}: {count}")
    print()

    # STEP 5: Simple Code Generation
    print("-" * 70)
    print("STEP 5: Simple Code Generation Demo")
